package gb

import (
	"gemu/pkg/cpu"
	"gemu/pkg/mmu"
)

// GameBoy represents the GameBoy hardware
type GameBoy struct {
	cpu cpu.CPU
	mmu mmu.MMU
}

func (gb *GameBoy) Init() {
	gb.cpu.Init()
	gb.mmu.Init()

	gb.mmu.Write(0xC000, 0xFD)
}
